        <p class="pt-20"><b>Establishment of Clubs:</b></p>
        <ul>
            <li><b>All students must register in the Extracurricular Learning Experience (ELE) Portal!</b></li>
            <li><b>All clubs must be approved by Student Affairs & Alumni (SAA).</b></li>
            <li>The number of club members to establish a club is <b>minimum 10.</b></li>
            <li>Following <b>establishment</b>, each club must submit its <b>statement of purpose</b> to SAA.</li>
            <li>Clubs will be under a <b>faculty advisor</b> who is associated with a faculty of similar purpose.</li>
            <li>Club’s <b>forums</b> must be approved by moderator and/or faculty advisor.</li>
            <li>Clubs will <b>recruit</b> new members in the beginning of semester.</li>
        </ul>

        <p class="pt-20"><b>Committee Members of Clubs:</b></p>
        <ul>
            <li><b>Committee</b> will be formed through a yearly regulated <b>election</b> by the club members. The moderator and SAA must be notified. In early stages the committee could be formed by SAA.</li>
            <li>A student can be <b>re-elected</b> for the same position in the committee after they serve a year.</li>
            <li>The moderator will <b>inspect</b> the committee and can disband the committee if they are inept or act against the policies.</li>
            <li>The committee of the clubs will consist of <b>minimum 3</b> and <b>maximum 6</b> members. The committee will consist of the following positions: </li>
            <ol class="alphabet-list">
                <li>President</li>
                <li>Vice President</li>
                <li>Secretary</li>
                <li>Treasurer</li>
                <li>Organizing Secretary</li>
                <li>Publicity Secretary</li>
            </ol>
            <li>The committee is required to meet once every <b>2 weeks</b>. Meetings can be called between club members when needed. Moderator must be notified and updated on meeting discussion and activities.</li>
        </ul>

