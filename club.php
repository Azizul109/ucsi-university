                <p class="pt-20"><b>Events:</b></p>
                <ul>
                    <li><b>Mandatory 1 event per semester each Club.</b></li>
                    <li><b>During each event, the committee must be present throughout the whole event.</b></li>
                    <li>Events require a <b>proposal</b> from the committee and must be authorized by SAA to be allowed to organize.</li>
                    <li><b>Name, ID Number and Programme/Faculty</b> of students must submitted to SAA when they <b>participate</b> in an event.</li>
                    <li>Students are required to <b>fill out and submit a registration and disclaimer form</b> before attending events off-campus.</li>
                    <li>After an event is completed, a <b>report</b> on the event must be submitted to SAA.</li>
                </ul>

                <p class="pt-20"><b>Expenses and Logistics:</b></p>
                <ul>
                    <li><b>Yearly budget and event budget are required to be submitted to the advisor and moderator for approval.</b></li>
                    <li>The <b>income and monetary</b> issues of each club must be accounted for in the beginning and end of semester. This will be handled by the committee and club members.</li>
                    <li>All earnings and/or budget will be <b>deposited</b> in the care of the treasurer of the club.</li>
                    <li>Any expense relating to <b>off-campus</b> expense are required to have a proper receipt.</li>
                    <li>All receipts for expenses must be kept and <b>presented</b> with the account for the deposit to SAA.</li>
                    <li>The SAA will perform an <b>audit</b> on the <b>deposit</b> and give approval accordingly to the club.</li>
                    <li>Required <b>logistic support</b> demands a formal notice from the committee <b>1 week</b> before events. All information about logistics is required to be in the formal notice.</li>
                    <li><b>Name, ID Number and Programme/Faculty</b> of committee member responsible for logistics must be submitted to SAA.</li>
                </ul>

                <p class="pt-20"><b>Required Materials:</b></p>
                <ul>
                    <li>All <b>Materials</b> must be <b>signed</b> by SAA.</li>
                    <li><b>Materials</b> such as Notices, posters, e-mails and invitations, etc of events must be sent and/or posted at least <b>1-3 weeks</b> in advance depending on the event.</li>
                </ul>
                <p class="pt-20"><b>Extracurricular Learning Experience (ELE) Points:</b></p>
                <p>Extracurricular Learning Experience (ELE) highlights the importance of experiential learning (i.e. outside classroom learning) that enables students to exercise pertinent soft skills like leading and managing teams, articulating ideas, strategic planning, and evaluation skills. It is mandatory for students to earn 300 ELE Points to graduate from UCSI University Bangladesh Branch Campus. These points are awarded to the students when they are involved in university club activities and events. Different extra-curricular activities and involvement from students will award them with points based on the extra-curricular activities and involvement. A Student can earn a total of 100 ELE points every year, any outstanding points won’t be counted after that year ends. The total number of points are tallied and calculated for each student towards their graduation.</p>

